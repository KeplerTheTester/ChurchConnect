    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_Message_of_day)
        {
            Toast.makeText(this, "pressed on Message of day", Toast.LENGTH_SHORT).show();
            Intent Message_of_dayIntent = new Intent(this, Message_of_day.class);
            startActivity(Message_of_dayIntent);

            // Handle the camera action
        }
        else if (id == R.id.nav_Calendar)
        {
            Toast.makeText(this, "pressed on Calendar", Toast.LENGTH_SHORT).show();
            Intent CalendarIntent = new Intent(this, CalendarActivity.class);
            startActivity(CalendarIntent);

        }
        else if (id == R.id.nav_Sign_Up)
        {

        }
        else if (id == R.id.nav_Communicate)
        {
            Toast.makeText(this, "Pressed on Communicate", Toast.LENGTH_SHORT).show();
            Intent CommunicateIntent = new Intent(this, CommunicateActivity.class);
            startActivity(CommunicateIntent);

        }
        else if (id == R.id.nav_share)
        {

        }
        else if (id == R.id.nav_about)
        {
            Toast.makeText(this, "pressed on About", Toast.LENGTH_SHORT).show();
            Intent AboutIntent = new Intent(this, AboutActivity.class);
            startActivity(AboutIntent);

        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }

